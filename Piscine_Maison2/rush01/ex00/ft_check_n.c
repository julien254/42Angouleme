int	ft_check_n(char *str)
{
	int	i;
	int	count;

	count = 0;
	i = 0;
	while (str[i])
	{
		if (str[i] >= '0' && str[i] <= '9')
			count++;
		i++;
	}
	if (count % 4 != 0)
		return (0);
	return (count / 4);
}
