int	ft_atoi(const char *str)
{
	int	i;
	int	r;
	int	nb;

	i = 0;
	r = 0;
	nb = 0;
	while ((str[i] == ' ') || (str[i] >= 9 && str[i] <= 13))
		i++;
	if ((str[i] == '-' || str[i] == '+') && \
	(str[i + 1] != '-' || str[i + 1] != '+'))
	{
		if (str[i] == '-')
			nb++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + str[i] - '0';
		i++;
	}
	if (nb > 0)
		return (-r);
	return (r);
}
