#include "push_swap.h"

int	main(int argc, char **argv)
{
	t_stack	*stack_a;
	t_stack	*stack_b;

	stack_a = NULL;
	stack_b = NULL;
	if (argc == 1)
		return (0);
	make_stack_a(&stack_a, argv);
	sort(&stack_a, &stack_b);
	free_stack(stack_a);
	return (0);
}
